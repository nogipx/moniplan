def load_messages() -> Dict[str, str]:
    """
    Загрузить текстовые сообщения для бота.

    Returns:
        Словарь с сообщениями.
    """
    messages_path = os.path.join(os.path.dirname(__file__), "messages.json")

    if os.path.exists(messages_path):
        try:
            with open(messages_path, "r", encoding="utf-8") as f:
                return json.load(f)
        except Exception as e:
            logger.error(f"Ошибка загрузки сообщений: {e}")

    return DEFAULT_MESSAGES


def save_messages(messages: Dict[str, str]) -> None:
    """
    Сохранить текстовые сообщения для бота.

    Args:
        messages: Словарь с сообщениями.
    """
    messages_path = os.path.join(os.path.dirname(__file__), "messages.json")

    try:
        with open(messages_path, "w", encoding="utf-8") as f:
            json.dump(messages, f, ensure_ascii=False, indent=2)

        logger.info("Сообщения успешно сохранены")
    except Exception as e:
        logger.error(f"Ошибка сохранения сообщений: {e}")


def generate_activation_code(length: int = 12) -> str:
    """
    Генерировать случайный код активации.

    Args:
        length: Длина кода.

    Returns:
        Строка со случайным кодом.
    """
    characters = string.ascii_uppercase + string.digits
    return ''.join(random.choice(characters) for _ in range(length))
