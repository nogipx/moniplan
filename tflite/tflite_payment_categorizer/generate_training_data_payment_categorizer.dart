        // Доходы 💰
        'Зарплата': 1,
        'Фриланс': 1,
        'Пассивный доход': 1,
        'Бизнес': 1,
        'Подарки и переводы': 1,
        'Социальные выплаты': 1,
        'Кэшбэк и бонусы': 1,
        'Продажа вещей': 1,
        'Призы и выигрыши': 1,

        // Расходы 💸
        // Базовые нужды
        'Жильё и коммунальные': 0,
        'Продукты': 0,
        'Транспорт': 0,
        'Автомобиль': 0,
        'Медицина': 0,
        'Связь и подписки': 0,

        // Личный бюджет
        'Одежда и обувь': 0,
        'Красота и здоровье': 0,
        'Развлечения': 0,
        'Путешествия': 0,
        'Обучение': 0,

        // Семья и дом
        'Дети и образование': 0,
        'Ремонт и обустройство': 0,
        'Домашние животные': 0,

        // Обязательства
        'Кредиты и долги': 0,
        'Страхование': 0,
        'Налоги и штрафы': 0,

        // Другие траты
        'Хобби и увлечения': 0,
        'Подарки и благотворительность': 0,
        'Рабочие расходы': 0,
        'Экстренные расходы': 0,
        'Инвестиции и накопления': 0,
      };

      // Для каждой категории генерируем синтетические данные
      for (final category in _categories) {
        // Получаем ключевые слова для категории
        final keywordMap = _getCategoryKeywords();
        final keywords = keywordMap[category] ?? [];

        print('Генерация данных для категории "$category"...');
        categoryCount[category] = 0;

        // Генерируем 2000 записей для каждой категории
        for (var i = 0; i < 2000; i++) {
          // Генерируем базовое название транзакции
          String title = _generateTransactionTitle(category, '', random);

          // Добавляем разнообразие в названия
          String transactionText = title;

          // С вероятностью 40% добавляем опечатку в название
          if (random.nextDouble() < 0.4) {
            transactionText = _applyDistortion(transactionText, random);
          }

          // С вероятностью 30% добавляем числа (например, даты, суммы, номера)
          if (random.nextDouble() < 0.3) {
            // Добавляем разные типы чисел
            final numberType = random.nextInt(5);
            switch (numberType) {
              case 0: // Дата
                final month = random.nextInt(12) + 1;
                final day = random.nextInt(28) + 1;
                transactionText = '$transactionText $day.$month';
                break;
              case 1: // Номер
                final num = random.nextInt(1000);
                transactionText = '$transactionText №$num';
                break;
              case 2: // Сумма
                final amount = (random.nextInt(10000) + 100) / 100;
                transactionText = '$transactionText $amount';
                break;
              case 3: // Год
                final year = 2020 + random.nextInt(5);
                transactionText = '$transactionText $year';
                break;
              case 4: // Просто число
                final num = random.nextInt(100);
                transactionText = '$transactionText $num';
                break;
            }
          }

          // С вероятностью 25% добавляем одно ключевое слово к названию
          if (keywords.isNotEmpty && random.nextDouble() < 0.25) {
            final keyword = keywords[random.nextInt(keywords.length)];

            // Применяем искажения к ключевому слову с вероятностью 30%
            final processedKeyword =
                random.nextDouble() < 0.3 ? _applyDistortion(keyword, random) : keyword;

            // Добавляем ключевое слово к названию
            transactionText = '$transactionText $processedKeyword';
          }

          // С вероятностью 15% добавляем сокращения или специальные символы
          if (random.nextDouble() < 0.15) {
            final symbolType = random.nextInt(4);
            switch (symbolType) {
              case 0: // Добавляем точки
                transactionText = transactionText.replaceAll(' ', '.');
                break;
              case 1: // Добавляем дефисы
                transactionText = transactionText.replaceAll(' ', '-');
                break;
              case 2: // Добавляем скобки
                if (transactionText.contains(' ')) {
                  final parts = transactionText.split(' ');
                  if (parts.length > 1) {
                    final idx = random.nextInt(parts.length - 1) + 1;
                    parts[idx] = '(${parts[idx]})';
                    transactionText = parts.join(' ');
                  }
                }
                break;
              case 3: // Добавляем сокращения
                if (transactionText.length > 5) {
                  final length = random.nextInt(3) + 3; // 3-5 символов
                  transactionText = transactionText.substring(
                    0,
                    min(length, transactionText.length),
                  );
                }
                break;
            }
          }

          // С вероятностью 10% переводим в верхний регистр
          if (random.nextDouble() < 0.1) {
            transactionText = transactionText.toUpperCase();
          }

          // С вероятностью 5% добавляем эмодзи
          if (random.nextDouble() < 0.05) {
            final emojis = [
              '💰',
              '💸',
              '🛒',
              '🏠',
              '🚗',
              '🍔',
              '👕',
              '💊',
              '📱',
              '✈️',
              '🎓',
              '👶',
              '🐶',
              '🏦',
              '🎁',
            ];
            final emoji = emojis[random.nextInt(emojis.length)];
            // Добавляем эмодзи в начало или конец
            if (random.nextBool()) {
              transactionText = '$emoji $transactionText';
            } else {
              transactionText = '$transactionText $emoji';
            }
          }

          // Определяем тип транзакции (1 для дохода, 0 для расхода)
          int type = categoryTypes[category] ?? 0;

          // Создаем запись
          final record = {'text': transactionText, 'type': type, 'category': category};

          trainingData.add(record);
          categoryCount[category] = (categoryCount[category] ?? 0) + 1;
        }

        print('Сгенерировано ${categoryCount[category]} записей для категории "$category"');
      }

      // Сохраняем синтетические данные
      final appDir = await getApplicationDocumentsDirectory();
      final file = File('${appDir.path}/synthetic_training_data.json');

      await file.writeAsString(jsonEncode(trainingData));

      print('Сгенерировано ${trainingData.length} синтетических записей для обучения');
      print('Распределение по категориям:');
      for (final category in _categories) {
        print('  $category: ${categoryCount[category]} записей');
      }
      print('Данные сохранены в файл: ${file.path}');

      // Создаем CSV файл для обучения модели
      final csvFile = File('${appDir.path}/synthetic_training_data.csv');
      final csvContent = StringBuffer();

      // Добавляем заголовок
      csvContent.writeln('text,type,category');

      // Добавляем данные
      for (final data in trainingData) {
        // Экранируем запятые и кавычки в тексте
        final escapedText = (data['text'] as String).replaceAll('"', '""');
        csvContent.writeln('"${escapedText}",${data['type']},"${data['category']}"');
      }

      // Сохраняем CSV файл
      await csvFile.writeAsString(csvContent.toString());

      print('Синтетические данные для обучения сохранены в CSV файл: ${csvFile.path}');
    } catch (e) {
      print('Ошибка при генерации синтетических данных: $e');
    }
  }

