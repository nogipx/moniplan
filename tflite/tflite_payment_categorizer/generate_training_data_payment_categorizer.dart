  /// Применяет искажения к ключевому слову
  String _applyDistortion(String keyword, Random random) {
    if (keyword.isEmpty) return keyword;

    // Выбираем тип искажения
    final distortionType = random.nextInt(8); // Увеличили до 8 типов искажений

    switch (distortionType) {
      case 0:
        // Опечатка: замена одной буквы
        if (keyword.length > 1) {
